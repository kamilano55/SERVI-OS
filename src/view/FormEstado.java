    public void readTable() {

        DefaultTableModel modelo = (DefaultTableModel) jTableEstado.getModel();
        modelo.setNumRows(0);
        EstadoDAO edao = new EstadoDAO();

        for (Estado e : edao.readAllEstado()) {
            modelo.addRow(new Object[]{
                e.getIdestado(),
                e.getNome(),
                e.getUf()
            });
        }
    }

    public void readTableForName(String nome) {

        DefaultTableModel modelo = (DefaultTableModel) jTableEstado.getModel();
        modelo.setNumRows(0);
        EstadoDAO edao = new EstadoDAO();

        for (Estado e : edao.readForName(nome)) {
            modelo.addRow(new Object[]{
                e.getIdestado(),
                e.getNome(),
                e.getUf()
            });
        }
    }

