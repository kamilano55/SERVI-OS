    private void jTableEstadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableEstadoMouseClicked
        // TODO add your handling code here:

// As linhas abaixo selecionam um item da tabela para ser alterado e copiam o item para os campos.
        Estado estado = new Estado();

        if (jTableEstado.getSelectedRow() != -1) {
            txtCodigo.setEnabled(true);
            txtNome.setEnabled(true);
            txtUf.setEnabled(true);

            if (FormMenu.lblUsuario.getText().equals("ADMGERAL")) {
                btnExcluir.setEnabled(true);
            } else {
                btnExcluir.setEnabled(false);
            }
            
            btnAtualizar.setEnabled(true);
            btnSalvar.setEnabled(false);
            btnLimpar.setEnabled(false);

            txtCodigo.setText(jTableEstado.getValueAt(jTableEstado.getSelectedRow(), 0).toString());
            txtNome.setText(jTableEstado.getValueAt(jTableEstado.getSelectedRow(), 1).toString());
            txtUf.setText(jTableEstado.getValueAt(jTableEstado.getSelectedRow(), 2).toString());
        }
    }//GEN-LAST:event_jTableEstadoMouseClicked

