    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if((txtQuantidade.getText().isEmpty())
                || (txtDescricao.getText().isEmpty())
                || (jComboBoxUnidade.getSelectedItem() == "Escolha")
                || (txtPreco.getText().isEmpty())){
            JOptionPane.showMessageDialog(null, "Verifique se todos os campos estão preenchido", "AVISO", JOptionPane.WARNING_MESSAGE);
            txtQuantidade.requestFocus();
            
            return;
        }else{
            Peca p = new Peca();
            PecaDAO dao = new PecaDAO();
            
            p.setQtd(Double.parseDouble(txtQuantidade.getText()));
            p.setDescricao(txtDescricao.getText());
            p.setUnidade(jComboBoxUnidade.getSelectedItem().toString());
            p.setPreco(Double.parseDouble(txtPreco.getText()));
            
            Os os = new Os();
            os.setIdos(Integer.parseInt(txtIdOs.getText()));
            p.setOs(os);
            
            dao.save(p);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

