    public void importaId(Os os) {
        txtIdOs.setText(Integer.toString(os.getIdos()));
        readPecaForIdOs(Integer.parseInt(txtIdOs.getText()));
        txtQuantidade.requestFocus();
    }

    public void readPecaForIdOs(int id) {
        DefaultTableModel mod = (DefaultTableModel) jTablePecas.getModel();
        mod.setNumRows(0);
        PecaDAO dao = new PecaDAO();

        for (Peca p : dao.readPecaForOs(id)) {
            mod.addRow(new Object[]{
                
                p.getIdpeca(),
                p.getQtd(),
                p.getDescricao(),
                p.getUnidade(),
                p.getPreco(),
                p.getOs()
            });
        }
    }

